package arithmeticOperators;

public class Subtraction {
    public static void main(String[] args) {
        /*
        PSEUDO CODE
        -Create 2 int variables and assign 15 and 10 to those
        -Find the variance (difference) of those variables and print
        -Expected result= Difference is: 5
         */

        int i1 = 15, i2 = 10;

        System.out.println("Variance of those numbers is: " + (i1 - i2)); // Variance of those numbers is: 5

        /*
        PSEUDO CODE
        -Create 3 int variables value1, value2, and value3 and assign 50, 15, and 10 to those
        -Find the result for value1-value2-value3 and print
        -Expected result= Difference is: 25
         */

        int value1 = 50, value2 = 15, value3 = 10;

        System.out.println("The result of value1-value2-value3 is: " + (value1-value2-value3));
    }
}
