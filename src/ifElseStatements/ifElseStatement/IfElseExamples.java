package ifElseStatements.ifElseStatement;

public class IfElseExamples {
    public static void main(String[] args) {
        boolean isWeekend = true;

        if(isWeekend){
            System.out.println("It is weekend");
        }
        else{
            System.out.println("It is not weekend");
        }

        boolean isThereASoccerGame = false;

        if(isThereASoccerGame){
            System.out.println("I will watch the game then");
        }
        else{
            System.out.println("Okay, I can go out");
        }

        System.out.println("End of program");
    }
}
