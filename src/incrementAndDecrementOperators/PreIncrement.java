package incrementAndDecrementOperators;

public class PreIncrement {
    public static void main(String[] args) {

        int number1 = 50;
        int number2 = ++number1;

        System.out.println("Number 1 is: " + number1); // 51
        System.out.println("Number 2 is: " + number2); // 51
    }
}
