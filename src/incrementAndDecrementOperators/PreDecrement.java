package incrementAndDecrementOperators;

public class PreDecrement {
    public static void main(String[] args) {
        int number1 = 50;
        int number2 = --number1;

        System.out.println("Number 1 is: " + number1); // 49
        System.out.println("Number 2 is: " + number2); // 49
    }
}
