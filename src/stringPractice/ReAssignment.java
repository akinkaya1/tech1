package stringPractice;

public class ReAssignment {
    public static void main(String[] args) {

        String name;
        int age;
        String address;

        name = "Raqi"; // name is = Raqi
        address = "Chicago IL";
        age = 19; // age is = 19

        name = "Smith"; // name is = Smith
        age = 27; // age is = 27

        System.out.println(name); //Smith
        System.out.println(age); //27
    }
}
