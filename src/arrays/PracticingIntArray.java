package arrays;

public class PracticingIntArray {
    /**
     -Create an int array and store ages in it
     -The size of array must be 5
     -Assign values to each element // 10, 23, 44, 53, 18
     -Finally, print all elements
     */
    public static void main(String[] args) {

        int[] ages = new int[5];
        ages[0] = 10;
        ages[1] = 23;
        ages[2] = 44;
        ages[3] = 53;
        ages[4] = 18;

        System.out.println(ages[0]);
        System.out.println(ages[1]);
        System.out.println(ages[2]);
        System.out.println(ages[3]);
        System.out.println(ages[4]);
    }
}
